public class IfStatement {
    public static void main(String[] args) {

        int age = 23;
        if(age>=75){
            System.out.println("Ok boomer!");
        }
        else if (age>=20){
            System.out.println("You are a teenager!");
        }
        else if(age>=18){
            System.out.println("You are an adult!");
        }
        else {
            System.out.println("You are not an adult!");
        }
    }
}
